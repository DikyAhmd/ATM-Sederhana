    private void textPINKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_textPINKeyPressed
        // TODO add your handling code here:
        String value = textPIN.getText();
        int l = value.length();
        if (evt.getKeyChar() >= '0' && evt.getKeyChar() <= '9') {
            textPIN.setEditable(true);
        } else {
            textPIN.setEditable(false);
            JOptionPane.showMessageDialog(null, "Hanya Angka yang diperbolehkan");
        }
        if(l>5){
            textPIN.setEnabled(false);
        }
    }//GEN-LAST:event_textPINKeyPressed

