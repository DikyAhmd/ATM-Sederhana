    public void comparePassword(){
        dataBank data = new dataBank();
        if(textPIN.getPassword().length==6){
            if(Arrays.equals(textPIN.getPassword(), textPIN2.getPassword())){
                int jawab = JOptionPane.showOptionDialog(this, 
                    "Apakah data anda sudah benar?", 
                    "Konfirmasi", 
                    JOptionPane.YES_NO_OPTION, 
                    JOptionPane.QUESTION_MESSAGE, null, null, null);
                if(jawab == JOptionPane.YES_OPTION){
                    AfterRegister after = new AfterRegister();
                    String pn = new String(textPIN.getPassword());
                    data.addDataToDatabase(pn, textNama.getText(), textNIM.getText());
                    JOptionPane.showMessageDialog(this, "Account has been created successfully");
                    after.setData(textNIM.getText(), "0");
                    after.setVisible(true);
                    dispose();
                }
            } else {
                JOptionPane.showMessageDialog(null, "PIN Tidak Sama");
                textPIN.setEnabled(true);
                textPIN2.setEnabled(true);
                textPIN.setText("");
                textPIN2.setText("");
            }
        }
        else{
            JOptionPane.showMessageDialog(this, "PIN harus 6 Angka");
        }
    }
    
    
    void cekIfNimExist(){
        String nim = new String(textNIM.getText());
        try{
            connection = DriverManager.getConnection("jdbc:mysql://localhost:3306/atm_sederhana", "Diky", "Dikypass123");
            statement = (Statement) connection.createStatement();
            String NM = "SELECT * FROM `data` WHERE NIM='"+textNIM.getText()+"'";
            ResultSet resultSet = statement.executeQuery(NM);
            
            while (resultSet.next()) {
                String NIM = resultSet.getString("NIM");
                if(nim.equalsIgnoreCase(NIM)){
                    JOptionPane.showMessageDialog(null, "NIM Sudah Terdaftar");
                    refresh();
                    flag=1;
                }
            }
            if (flag == 0) {
                comparePassword();
            }
            
        } 
        catch(SQLException throwables){
            throwables.printStackTrace();
        }
    }


